function renderTimelineCards(events) {
  return events.map((event) => `
    <article class="timeline-card" data-level="${event.severity}">
      <div class="timeline-row">
        <strong>${event.time} · ${l(event.title)}</strong>
        <span class="level-tag" data-level="${event.severity}">${severityLabel(event.severity)}</span>
      </div>
      <div class="pill-row"><span class="mini-pill">${l(event.mood)}</span></div>
      <p>${l(event.note)}</p>
    </article>
  `).join("")
}

function relatedEventsForAlert(alert) {
  return demoData.events.filter((event) => alert.relatedEventIds.includes(event.id))
}

